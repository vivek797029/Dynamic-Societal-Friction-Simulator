SEVERITY_MAP = {"low": 1, "medium": 2, "high": 3, "very_high": 4, "critical": 5}

FRICTION_TYPES = [
    "economic_protest", "identity_conflict", "political_protest",
    "regime_change", "armed_conflict", "civil_war", "military_coup",
    "police_brutality_protest", "social_rights_protest", "economic_crisis",
    "racial_justice_protest", "religious_conflict", "separatist_movement"
]


class HistoricalCaseDB:
    """Database manager for historical friction cases."""

    def __init__(self):
        self.cases = HISTORICAL_CASES
        self.indicator_meta = INDICATOR_METADATA

    def get_all_cases(self) -> List[Dict]:
        return self.cases

    def get_cases_by_type(self, friction_type: str) -> List[Dict]:
        return [c for c in self.cases if c["type"] == friction_type]

    def get_cases_by_country(self, country: str) -> List[Dict]:
        return [c for c in self.cases if c["country"].lower() == country.lower()]

    def get_cases_by_severity(self, min_severity: str = "high") -> List[Dict]:
        threshold = SEVERITY_MAP.get(min_severity, 3)
        return [c for c in self.cases if SEVERITY_MAP.get(c["severity"], 0) >= threshold]

    def get_indicator_names(self) -> List[str]:
        return list(INDICATOR_METADATA.keys())

    def get_training_features(self) -> tuple:
        """Extract features and labels for ML training."""
        features = []
        labels = []
        for case in self.cases:
            feature_vec = [case["indicators"].get(k, 0) for k in INDICATOR_METADATA.keys()]
            severity_score = SEVERITY_MAP.get(case["severity"], 2)
            labels.append(severity_score)
            features.append(feature_vec)
        return features, labels

    def get_case_by_id(self, case_id: str) -> Optional[Dict]:
        for case in self.cases:
            if case["id"] == case_id:
                return case
        return None

    def to_json(self, filepath: str):
        with open(filepath, "w") as f:
            json.dump(self.cases, f, indent=2)

    @classmethod
    def from_json(cls, filepath: str):
        db = cls()
        with open(filepath) as f:
            db.cases = json.load(f)
        return db
